public class Calculadora {
    public double suma(double a , double b){//operacion de suma 
        return a+b;
    }
    public double resta(double a , double b){//operacion de resta 
        return a-b;
    }
    public double div(double a , double b){//operacion de division
        return a%b;
    }
    public double mult(double a , double b){//operacion de multiplicacion
        return a*b;
    }
    
}
